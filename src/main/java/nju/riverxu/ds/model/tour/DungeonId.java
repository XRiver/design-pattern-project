package nju.riverxu.ds.model.tour;

public class DungeonId {
    private int id;
    private String name;

    public DungeonId(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
